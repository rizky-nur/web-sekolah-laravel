namespace App\Http\Controllers;

use Illuminate\Http\Request;
use \App\Models\contact;
use Illuminate\Support\Facades\Storage;

class dashboardcontactController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('dashboard.contact.index',[
            'title' => 'contact',
            'contacts' => contact::all()
        ]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return view('dashboard.contact.show',[
            'title' => 'Detail contact',
            'contact' => contact::find($id)
        ]);
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        return view('dashboard.contact.edit',[
            'title' => 'Edit Detail contact',
            'contact' => contact::find($id)
        ]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $rules =[
            'title' => ['required','max:255'],
            'description' => ["required"],
        ];
        $validate = $request->validate($rules);

        
        contact::where('id', $id)->update($validate);

        return redirect('/dashboard/contact')->with('success', 'Post has been updated');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        contact::where('id', $id)->delete();

        return redirect('/dashboard/contact')->with('success', 'Post has been deleted');
    }
}